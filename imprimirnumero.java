public class imprimirnumero {
    public static void main(String[] args) {

        // Um abaixo do outro
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        // Um ao lado do outro
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}